package XML_JSON.XML.HomeWork.Task1;

public class Cat {
    private String name;
    private int age;
    private String breed;
    private int weight;

    public Cat(String name, int age, String breed, int weight) {
        this.name = name;
        this.age = age;
        this.breed = breed;
        this.weight = weight;
    }

    public String getName() {
        return name;
    }

    public int getAge() {
        return age;
    }

    public String getBreed() {
        return breed;
    }

    public int getWeight() {
        return weight;
    }
}
